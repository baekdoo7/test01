<p>29일 잠실구장에서 열린 두산과 롯데의 주중 2연전 첫 경기는 예상대로 뜨거웠다. 양팀이 밀고 당기는 접전을 펼쳤다. 1회 1점씩 나란히 올린 뒤, 3회말 두산이 3점을 추가하며 쐐기를 박는 듯했지만 롯데는 5회초 2점, 7회초 2점을 올리며 5-4로 역전해 분위기를 고조시켰다. 그러나 두산이 7회말 류지혁의 솔로포로 따라붙으며 승부를 5-5 원점으로 돌렸다.</p>
