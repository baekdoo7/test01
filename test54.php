 <br /> (세종=연합뉴스) 이세원 이대희 민경락 기자 = 작년 12월 일자리 증가 폭이 3만4천명에 그치며 지난해 연간 일자리 증가 폭도 금융위기 시절인 2009년 이후 가장 낮은 9만7천명을 기록했다.
 <br />
 <br /> 통계청이 9일 발표한 2018년 12월 및 연간 고용동향에 따르면 지난달 취업자는 2천663만8천명으로 1년 전보다 3만4천명 늘어나는 데 그쳤다.
 <br />
 <br /> 이에 따라 연간 취업자는 2천682만2천명으로 1년 전보다 9만7천명 증가해 10만명을 밑돌았다. 
 <br />
 <br /> 이는 금융위기 여파가 있었던 2009년 8만7천명이 감소한 이후 9년 만에 가장 낮은 수준이다.
 <br />
 <br /> 작년 실업자는 107만3천명이었다. 통계 기준을 바꾼 이래 연도별 비교가 가능한 2000년 이후로는 가장 많다. 실업자 수는 2016년부터 3년 연속 100만명 이상을 기록하고 있다.
 <br />
 <br /> 작년 실업률은 3.8%로 전년보다 0.1%포인트 올랐고, 고용률은 60.7%로 2017년보다 0.1%포인트 내렸다. 
 <br />
 <br /> 빈현준 통계청 고용통계과장은 &quot;취업자 증가 폭 축소는 생산가능인구 감소와 인구증가 폭 축소, 자동차 등 제조업 부진, 서비스업 구조조정 등이 겹친 영향&quot;이라며 &quot;다만 20대 후반을 중심으로 청년층은 다소 개선의 여지가 있다&quot;고 분석했다.
