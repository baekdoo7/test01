<p>관중의 욕설과 야유에 경기가 잠시 중단되기도 했다. 판정 번복에 불만을 품은 롯데 팬들이 8회초 수비에 나온 두산 좌익수 김재환에게 몰려가 욕설을 했다. 3루 롯데 응원석에서도 단체 야유가 쏟아졌다. 이에 분개한 2루수 오재원이 응원석을 향해 달려가는 것을 유격수 류지혁이 제지하기도 했다.</p>

<p>경기는 두산이 8회말 1점을 더해 7-5로 승리하며 6연승 행진을 이어갔다. 두산은 이달에만 19승(5패)를 수확해 구단 신기록도 세웠다.</p>

<p>경기가 끝난 뒤 최수원 2루심은 박근영 3루심의 7회말 판정 번복에 대해 &ldquo;포스 아웃 상황이 맞았지만 3루수 김동한의 발이 떨어진 것을 늦게 확인했다. 그래서 아웃 선언을 했다가 이내 정정한 것&rdquo;이라고 설명했다.</p>

